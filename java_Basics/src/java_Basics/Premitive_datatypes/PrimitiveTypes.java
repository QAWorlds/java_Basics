package java_Basics.Premitive_datatypes;
public class PrimitiveTypes {
    public static void main(String[] args) {
        // int
    	
    //        how can we use data types in selenium java automation	
    	
    	
    	
        int myInt = 100;
        System.out.println("Integer value: " + myInt);

        // double
        double myDouble = 3.14159;
        System.out.println("Double value: " + myDouble);

        // float
        float myFloat = 5.75f;  // Note the 'f' suffix
        System.out.println("Float value: " + myFloat);

        // char
        char myChar = 'A';
        System.out.println("Character value: " + myChar);

        // boolean
        boolean isJavaFun = true;
        System.out.println("Is Java fun? " + isJavaFun);

        // byte
        byte myByte = 127;
        System.out.println("Byte value: " + myByte);

        // short
        short myShort = 32000;
        System.out.println("Short value: " + myShort);

        // long
        long myLong = 10000000000L;  // Note the 'L' suffix
        System.out.println("Long value: " + myLong);
    }
}
